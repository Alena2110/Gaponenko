    public static String numberComposition(int number) { //–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Å–æ—Å—Ç–∞–≤–∞ —á–∏—Å–ª–∞
        StringBuilder result = new StringBuilder();
        while (number > 0) {
            result.append(" ").append(number % 10);
            number /= 10;
        }
        return result.reverse().toString();
    }

